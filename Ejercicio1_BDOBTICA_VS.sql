CREATE DATABASE `BDOBTICA_VS`;

USE `BDOBTICA_VS`;

CREATE TABLE `TBL_PAIS`
(
	ID_PAIS INT UNSIGNED AUTO_INCREMENT NOT NULL,
    DESCRIPCION VARCHAR(60) UNIQUE NOT NULL,
    PRIMARY KEY(ID_PAIS)
);

CREATE TABLE `TBL_CIUDAD`
(
	ID_CIUDAD INT UNSIGNED AUTO_INCREMENT NOT NULL,
    DESCRIPCION VARCHAR(60) UNIQUE NOT NULL,
    ID_PAIS INT UNSIGNED NOT NULL,
    PRIMARY KEY(ID_CIUDAD),
    CONSTRAINT FK_PAIS_CIUDAD
	FOREIGN KEY (ID_PAIS) REFERENCES TBL_PAIS(ID_PAIS)
);

CREATE TABLE `TBL_DIRECCION`
(
	ID_DIRECCION INT UNSIGNED AUTO_INCREMENT PRIMARY KEY,
    CALLE VARCHAR(60),
    NUMERO VARCHAR(10),
    PISO INT UNSIGNED,
    PUERTA INT UNSIGNED,
    ID_PAIS INT UNSIGNED NOT NULL,
    ID_CIUDAD INT UNSIGNED NOT NULL,
    CONSTRAINT FK_TBL_DIRECCION_TBL_PAIS
    FOREIGN KEY (ID_PAIS) REFERENCES TBL_PAIS(ID_PAIS),
    CONSTRAINT FK_TBL_DIRECCION_TBL_CIUDAD
    FOREIGN KEY (ID_CIUDAD) REFERENCES TBL_CIUDAD(ID_CIUDAD)
);

CREATE TABLE `TBL_PROVEEDOR` 
(
	ID_PROVEEDOR INT UNSIGNED AUTO_INCREMENT,
    NOMBRE VARCHAR(150),
    TELEFONO VARCHAR(9),
    FAX VARCHAR(9),
    NIF VARCHAR(9),
    ID_DIRECCION INT UNSIGNED NOT NULL,
    PRIMARY KEY(ID_PROVEEDOR),
    CONSTRAINT FK_TBL_PROVEEDOR_TBL_DIRECCION
    FOREIGN KEY (ID_DIRECCION) REFERENCES TBL_DIRECCION(ID_DIRECCION)
);


CREATE TABLE `TBL_MARCA`
(
	ID_MARCA INT UNSIGNED AUTO_INCREMENT NOT NULL,
    DESCRIPCION VARCHAR(100),
    PRIMARY KEY(ID_MARCA)
);

/* Tabla para Asignar y verificar las marcas que le pueden comprar a cada proveedor */

CREATE TABLE `TBL_PROVEEDOR_MARCA`
(
	ID_PROV_MARCA INT UNSIGNED AUTO_INCREMENT,
    ID_PROVEEDOR INT UNSIGNED NOT NULL,
    ID_MARCA INT UNSIGNED NOT NULL UNIQUE,
    PRIMARY KEY(ID_PROV_MARCA),
    CONSTRAINT FK_TBL_PROV_MARCA_TBL_PROVEEDOR
    FOREIGN KEY (ID_PROVEEDOR) REFERENCES TBL_PROVEEDOR(ID_PROVEEDOR),
	CONSTRAINT FK_TBL_PROV_MARCA_TBL_MARCA
    FOREIGN KEY (ID_MARCA) REFERENCES TBL_MARCA(ID_MARCA)
);

CREATE TABLE `TBL_GAFA`
(
	ID_GAFA INT UNSIGNED AUTO_INCREMENT NOT NULL,
    ID_MARCA INT UNSIGNED NOT NULL,
    GRADUACION INT,
    TIPO_MONTURA enum('FLOTANTE','PASTA', 'METALICA'),
    COLOR_MONTURA VARCHAR(20),
    COLOR_VIDRIO VARCHAR(20),
    PRECION FLOAT UNSIGNED,
    PRECIO_VENTA FLOAT UNSIGNED,
    ID_PROVEEDOR INT UNSIGNED NOT NULL,
    PRIMARY KEY(ID_GAFA),
    CONSTRAINT FK_TBL_GAFA_TBL_MARCA
    FOREIGN KEY (ID_MARCA) REFERENCES TBL_MARCA(ID_MARCA),
    CONSTRAINT FK_TBL_GAFA_TBL_PROVEEDOR
    FOREIGN KEY (ID_PROVEEDOR) REFERENCES TBL_PROVEEDOR(ID_PROVEEDOR)
);
CREATE TABLE `TBL_COMPRA`
(
	ID_COMPRA INT UNSIGNED AUTO_INCREMENT NOT NULL,
    FACTURA_COMPRA VARCHAR(10) UNIQUE NOT NULL,
    ID_PROVEEDOR INT UNSIGNED NOT NULL,
    FECHA DATE,
    PRIMARY KEY(ID_COMPRA),
    CONSTRAINT FK_TBL_COMPRA_TBL_PROVEEDOR
    FOREIGN KEY (ID_PROVEEDOR) REFERENCES TBL_PROVEEDOR(ID_PROVEEDOR)
);

CREATE TABLE `TBL_DETALLECOMPRA`
(
	ID_DETALLE INT UNSIGNED AUTO_INCREMENT NOT NULL,
    ID_GAFA INT UNSIGNED,
    CANTIDAD INT UNSIGNED,
    COSTO FLOAT UNSIGNED,
    ID_COMPRA INT UNSIGNED,
    PRIMARY KEY(ID_DETALLE),
    CONSTRAINT FK_TBL_DETALLECOMPRA_TBL_GAFA
    FOREIGN KEY (ID_GAFA) REFERENCES TBL_GAFA(ID_GAFA),
    CONSTRAINT FK_TBL_DETALLECOMPRA_TBL_COMPRA
    FOREIGN KEY (ID_COMPRA) REFERENCES TBL_COMPRA(ID_COMPRA)
);

CREATE TABLE `TBL_VENDEDOR`
(
	ID_VENDEDOR INT UNSIGNED AUTO_INCREMENT NOT NULL,
    NOMBRE VARCHAR(50),
    APELLIDO VARCHAR(50),
    PRIMARY KEY(ID_VENDEDOR)
);

CREATE TABLE `TBL_CLIENTE`
(
	ID_CLIENTE INT UNSIGNED AUTO_INCREMENT NOT NULL,
    NOMBRE VARCHAR(60),
    DIRECION_POSTAL VARCHAR(10),
    TELEFONO VARCHAR(9) NOT NULL,
    EMAIL VARCHAR(30),
    FECHA_REG DATETIME,
    RECOMENDADO_POR INT UNSIGNED,
    PRIMARY KEY (ID_CLIENTE),
    CONSTRAINT FK_TBL_CLIENTE_TBL_CLIENTE
    FOREIGN KEY (RECOMENDADO_POR) REFERENCES TBL_CLIENTE(ID_CLIENTE)
);

CREATE TABLE `TBL_VENTA`
(
	ID_VENTA INT UNSIGNED AUTO_INCREMENT NOT NULL,
    FACTURA_VENTA VARCHAR(10) UNIQUE NOT NULL,
	ID_VENDEDOR INT UNSIGNED NOT NULL,
    ID_CLIENTE INT UNSIGNED NOT NULL,
    FECHA_REGISTRO DATETIME,
    PRIMARY KEY(ID_VENTA),
    CONSTRAINT FK_TBL_VENTA_TBL_PROVEEDOR
    FOREIGN KEY (ID_VENDEDOR) REFERENCES TBL_VENDEDOR(ID_VENDEDOR),
    CONSTRAINT FK_TBL_VENTA_TBL_CLIENTE
    FOREIGN KEY (ID_CLIENTE) REFERENCES TBL_CLIENTE(ID_CLIENTE)
);

CREATE TABLE `TBL_DETALLE_VENTA`
(
	ID_DETALLE_VENTA INT UNSIGNED AUTO_INCREMENT NOT NULL,
    ID_VENTA INT UNSIGNED NOT NULL,
    ID_GAFA INT UNSIGNED NOT NULL,
    CANTIDAD INT UNSIGNED,
    PRECIO FLOAT UNSIGNED,
    PRIMARY KEY(ID_DETALLE_VENTA),
    CONSTRAINT FK_TBL_DETALLE_VENTA_TBL_VENTA
	FOREIGN KEY (ID_VENTA) REFERENCES TBL_VENTA(ID_VENTA),
    CONSTRAINT FK_TBL_DETALLE_VENTA_TBL_GAFA
    FOREIGN KEY (ID_GAFA) REFERENCES TBL_GAFA(ID_GAFA)
);
